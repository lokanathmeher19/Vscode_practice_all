daynum=int(input("Enter the day number (1-7): "))

if daynum==1:
    print("Monday")
elif daynum==2:
    print("Tuesday")
elif daynum==3:
    print("Wednesday")
elif daynum==4:
    print("Thursday")
elif daynum==5:
    print("Friday")
elif daynum==6:
    print("Saturday")
else:
    print("Sunday")